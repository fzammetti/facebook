    <h1>K&G Arcade - Help</h1>
    <br>
    Playing the game is pretty simple, and the on-screen instructions pretty
    much say it all.  The one thing to remember is you'll need to click with
    your mouse somewhere inside the game area, or the keys you press won't
    register with the game.
    <br><br>
    Other than that, use the left and right keys to select a game, then space
    to start.  To exit a game and return to the game selection screen, press
    return.
    <br><br>
    To play Cosmic Squirrel, move the squirrel with the arrow keys to try and
    get to the acorn, avoiding the comets, aliens and asteroids.  Play until
    you decide to stop.
    <br><br>
    To play Deathrap, use the arrow keys to jump on the tiles, trying to find
    a safe path to the door on the far side.  Play until you decide to stop.
    <br><br>
    To play Refluxive, use the left and right arrow keys to move the paddle
    and keep the three... umm... things... bouncing.  The game ends when you
    have let all three things get past your paddle.
    <br><br>
